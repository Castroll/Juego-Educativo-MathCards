    void intento() //OCUPARE ESTE METODO PARA GUARDAR LOS DATOS CUANDO TERMINE LA PARTIDA
    {
        int partida = 2;
        int detener = partida + 1;
        String[] datos = {"Hola","que","pedo"};
        for (int i = partida; i < detener; i++) 
        {
            for (int j = 0; j < 3; j++) 
            {
                Constructor_Menu.Individuales[i][j] = datos[j];
            }
        }
    }
    
    void CargarDatos()
    {
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
        modelo.setRowCount(Constructor_Menu.Individuales.length);
        
        for (int i = 0; i < Constructor_Menu.Individuales.length; i++) 
        {
            for (int j = 0; j < 3; j++) 
            {
                modelo.setValueAt(Constructor_Menu.Individuales[i][j], i, j);
            }
        }
    }

